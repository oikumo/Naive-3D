export class Pallete {
    constructor() {
        this.color = {
            0 : parseInt(0xFF0000FF), // red
            1 : parseInt(0xFFFF0000), // blue
            2 : parseInt(0xFF00FF00), // green
            3 : parseInt(0xFF00FFFF), // yellow
            4 : parseInt(0xFF000000), // black
        }
    }
}